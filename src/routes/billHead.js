"use strict";
const express = require("express");
const router = express.Router();
const auth = require("../middleware/middleware.auth");
const winston = require("winston");
const errMW = require("../middleware/middleware.error");

const {
  createBillHead,
  getAllBillHead,
  getBillHeadByID,
  updateBillHead,
  deleteBillHead,
} = require("../controllers/billHead");

// CREATE BillHead
router.post("/create", [auth], async (req, res) => {
  try {
    winston.info(`Creating BillHead : ${req.body.name}`);
    const result = await createBillHead(req);
    if (result.status) {
      return res.status(200).send({
        status: result.status,
        data: result.data,
        message: result.message,
      });
    } else {
      if (result.errorCode === 400) {
        errMW(result.message, req, res);
      } else {
        return res.status(result.errorCode).send({
          status: result.status,
          data: result.data,
          message: result.message,
        });
      }
    }
  } catch (error) {
    errMW(error, req, res);
  }
});

